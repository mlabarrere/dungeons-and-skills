/* ==========================================================================
   cli.mjs — the skill's deterministic wrapper around the D&D 2024 engine.
   HAND-AUTHORED (not generated by build-bundles). Reuses resolver.mjs +
   build-character.mjs (generated copies) and the data/ catalogue.

   Commands:
     node engine/cli.mjs options <answers.json>            list the next legal choices
     node engine/cli.mjs build   <answers.json> [--lang en] answers -> model -> sheet + lint
     node engine/cli.mjs check   <model.character.json>     lint/compute an existing model

   Exit code is non-zero when the sheet has rules errors. This is the tool the
   skills MUST call instead of computing D&D math from memory. See ../rules/grounding.md.
   ========================================================================== */
